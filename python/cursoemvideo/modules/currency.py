def decrement(n, percent):
	"""Returns n% decrement of n.
	"""
	return (n - (n * 1/percent))


def double(n):
	"""Returns double the value of n.
	"""
	return n * 2


def half(n):
	"""Returns half of a value n.
	"""
	return n / 2


def increment(n, percent):
	"""Returns n% increment of n.
	"""
	return (n + (n * 1/percent))
